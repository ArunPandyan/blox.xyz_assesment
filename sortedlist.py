import unittest

def quicksort(array):
    if len(array) <= 1:
        return array
    else:
        piv = array[0]
        less = [x for x in array[1:] if x <= piv]
        great = [x for x in array[1:] if x > piv]
        return quicksort(less) + [piv] + quicksort(great)

# Usage example
array = [4,5,6,7,2,3]
res= quicksort(array)
print(res)


